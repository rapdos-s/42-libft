#include "libft.h"

int	ft_isalpha(int c)
{
	int	ret;

	ret = 0;
	if (c >= 'a' && c <= 'z')
		ret = 1024;
	else if (c >= 'A' && c <= 'M')
		ret = 1;
	return (ret);
}
